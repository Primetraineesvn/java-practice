package datatype;

public class DataType {

    public static void main(String[] args) {
        //Numerical data type = byte, int, short, long
        //floating point    -- double, float
        //Non numerical     -- character, boolean


        // Variable - It is a container which store the value

        // declaring variable with data type
        int a = 10;
        int b = 20;
        float c = 10.5f;
        double d = 10.41;
        String name = "Prime";
        String name1 = "Hello world";
        char x = 'H';
        boolean result = true;
        boolean result1 = false;
       // calling variables in to print statement
        System.out.println(a);//10
        System.out.println(b);//20
        System.out.println(c); //10.5
        System.out.println(d); //10.41
        System.out.println(name); //Prime
        System.out.println(name1); //Hello world
        System.out.println(x); //H
        System.out.println(result);//true
        System.out.println(result1);//false
// console panel
    }
}
